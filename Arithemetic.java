package Wipro_Talent_Next;
// Write a program to demonstrate working of arithmetic operators.

public class Arithemetic {
    public static void main(String[] args) {
        int a = 15, b = 4;

        System.out.println("a = " + a + ", b = " + b);

        // Addition
        System.out.println("a + b = " + (a + b));

        // Subtraction
        System.out.println("a - b = " + (a - b));

        // Multiplication
        System.out.println("a * b = " + (a * b));

        // Division
        System.out.println("a / b = " + (a / b));

        // Modulus
        System.out.println("a % b = " + (a % b));
    }
}
